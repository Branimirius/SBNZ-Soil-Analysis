package sbnz.integracija.example.facts;

public class Fertilizer {
	private int N;
	private int P;
	private int K;
	
	
	public Fertilizer(int n, int p, int k) {
		super();
		N = n;
		P = p;
		K = k;
	}
	
	public int getN() {
		return N;
	}
	public void setN(int n) {
		N = n;
	}
	public int getP() {
		return P;
	}
	public void setP(int p) {
		P = p;
	}
	public int getK() {
		return K;
	}
	public void setK(int k) {
		K = k;
	}
	
	
}
